"""LGPD: encrypt sensitive patient PII and clinical free-text at rest.

Mirrors the established encryption pattern (ai.0007 for the AI scribe
transcription): for every newly-encrypted column we first change the field type
(schema), then re-save existing rows so the library writes the ciphertext (data).

Encrypted here:
  * Patient: full_name, social_name, phone, email, address, notes
  * MedicalHistory.notes
  * Encounter.chief_complaint
  * SOAPNote: subjective, objective, assessment, plan
  * ClinicalDocument.content

Because encrypted columns hold opaque, non-deterministic ciphertext they can no
longer be DB-indexed/ordered/searched. The two Patient indexes that referenced
full_name are dropped, the default ordering moves to medical_record_number, and
a replacement (is_active, medical_record_number) index is added. Name search is
handled in Python (see emr/filters.py). whatsapp is intentionally left plaintext
(indexed routing/dedup key for the WhatsApp subsystem).

The schema/index operations below are exactly as generated by makemigrations;
the trailing RunPython performs the one-time re-encryption of existing rows and
is read-safe before it runs (a decrypt failure on legacy plaintext falls back to
the plaintext value).
"""

