# (model_name, [field, ...]) to re-save so existing plaintext gets encrypted.
_REENCRYPT = [
    ("Patient", ["full_name", "social_name", "phone", "email", "address", "notes"]),
    ("MedicalHistory", ["notes"]),
    ("Encounter", ["chief_complaint"]),
    ("SOAPNote", ["subjective", "objective", "assessment", "plan"]),
    ("ClinicalDocument", ["content"]),
]


def encrypt_existing_pii(apps, schema_editor):
    # Use the real models (not historical) so the EncryptedField descriptors run.
    from apps.emr import models as emr_models

    for model_name, fields in _REENCRYPT:
        model = getattr(emr_models, model_name)
        for obj in model.objects.all():
            obj.save(update_fields=fields)


